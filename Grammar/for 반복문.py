# range는 몇번 반복하라고 설정해주는것 0~5번
for i in range(0,5) :
    print('K5 있어요')

print('----------------------------------------------------------')

중고차들 = ['K5', 'BMW', 'Tico']
print(중고차들[0])
print(중고차들[1])
print(중고차들[2])

print('----------------------------------------------------------')

for i in 중고차들 :
    print(i)

print('----------------------------------------------------------')

# 반복문을 이용한 구구단 출력
for i in range(2,10):
    for j in range(1,10):
        print(i * j)



